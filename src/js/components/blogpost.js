class Blogpost {
    constructor (
        id,
        blogName,
        author,
        date,
        paragraph1,
        content,
        link,
        image,
        imageAlt
    ) {
    this.id = id;
    this.blogName = blogName;
    this.author = author;
    this.date = date;
    this.paragraph1 = paragraph1;
    this.content = content;
    this.link = link;
    this.image = image;
    this.imageAlt = imageAlt;
    }
}
export default Blogpost;